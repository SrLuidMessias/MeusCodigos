# Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a sua média.
# Avaliações
nota1=float(input('sua nota da 1 avaliação:'))
nota2=float(input('sua nota da 2 avaliação:'))
nota3=float(input('sua nota da 3 avaliação:'))
nota4=float(input('sua nota da 4 avaliação:'))
med=(nota1+nota2+nota3+nota4)/4
print('sua média escolar é {}'.format(med))
